#include "libft.h"

char		*ft_strcat(char *dst, const char *src)
{
	size_t	i;
	size_t	n;

	i = 0;
	n = 0;
	while (dst[i] != '\0')
		i++;
	while (src[n] != '\0')
	{
		dst[i] = src[n];
		i++;
		n++;
	}
	dst[i] = '\0';
	return (dst);
}
