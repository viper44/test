#include "libft.h"

void			ft_w_counter(const char *s)
{
	int			i;
	int			n;

	i = 0;
	n = 0;
	while (s[i] != '\0')
	{
		if ((s[0] != ' ' && i == 0) || (s[i] != ' ' && s[i - 1] == ' '))
			n++;
		i++;
	}
	ft_putnbr(n);
	ft_putchar('\n');
}
